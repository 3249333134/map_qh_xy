<template>
  <view 
    class="content-area" 
    :style="{ height: height + 'px', bottom: 0 }"
  >
    <!-- ÊãñÂä®Âå∫ÂüüÔºàÂåÖÂê´ÊãñÂä®Êù°ÂíåÊêúÁ¥¢Ê°ÜÔºâ -->
    <view 
      class="drag-area"
      @touchstart="onDragStart"
      @touchmove="onDrag"
      @touchend="onDragEnd"
    >
      <!-- ÊãñÂä®Êù° -->
      <view class="drag-handle">
        <view class="drag-indicator"></view>
      </view>
      
      <!-- ÊêúÁ¥¢Ê°Ü -->
      <view class="search-box">
        <view class="search-input-wrapper">
          <text class="search-icon">üîç</text>
          <input 
            class="search-input" 
            placeholder="ÊêúÁ¥¢" 
            confirm-type="search"
            @input="onSearchInput"
          />
        </view>
      </view>
    </view>
    
    <!-- ÂàÜÁ±ªÈÄâÈ°πÂç° -->
    <scroll-view 
      class="category-tabs" 
      scroll-x 
      show-scrollbar="false"
    >
      <view 
        v-for="category in categories" 
        :key="category.id"
        :class="['category-tab', { active: category.id === activeCategory }]"
        @tap="onCategoryChange(category.id)"
      >
        {{ category.name }}
      </view>
    </scroll-view>
    
    <!-- Âç°ÁâáÂÜÖÂÆπÂå∫ -->
    <scroll-view 
      class="cards-container"
      scroll-y
      @scrolltolower="onLoadMore"
      @scroll="onScroll"
      :scroll-top="scrollTop"
      :scroll-with-animation="true"
    >
      <view class="cards-grid">
        <!-- Â∑¶ÂàóÂç°Áâá -->
        <view class="cards-column">
          <card-item 
            v-for="(item, index) in leftColumnData" 
            :key="'left-' + (item._id || '') + '-' + index"
            :index="index"
            :card-data="item"
            :height="getColumnItemHeight('left', index)"
            column-type="left"
            @card-tap="onCardTap"
          />
        </view>
        
        <!-- Âè≥ÂàóÂç°Áâá -->
        <view class="cards-column">
          <card-item 
            v-for="(item, index) in rightColumnData" 
            :key="'right-' + (item._id || '') + '-' + index"
            :index="leftColumnData.length + index"
            :card-data="item"
            :height="getColumnItemHeight('right', index)"
            column-type="right"
            @card-tap="onCardTap"
          />
        </view>
      </view>
      
      <!-- Âä†ËΩΩÊõ¥Â§ö -->
      <view class="loading-more" v-if="isLoading">
        <text>Âä†ËΩΩ‰∏≠...</text>
      </view>
      
      <!-- Âä†ËΩΩÂÆåÊàêÊèêÁ§∫ -->
      <view class="loading-done" v-if="!hasMoreData && mapData.length > 0">
        <text>Â∑≤Âä†ËΩΩÂÖ®ÈÉ®ÂÜÖÂÆπ</text>
      </view>
    </scroll-view>
  </view>
</template>

<script>
import CardItem from './CardItem.vue'

export default {
  components: {
    CardItem
  },
  props: {
    height: {
      type: Number,
      required: true
    },
    searchBoxHeight: {
      type: Number,
      required: true
    },
    minContentHeight: {
      type: Number,
      required: true
    },
    categories: {
      type: Array,
      required: true
    },
    activeCategory: {
      type: String,
      required: true
    },
    mapData: {
      type: Array,
      required: true
    },
    isLoading: {
      type: Boolean,
      default: false
    },
    hasMoreData: {
      type: Boolean,
      default: true
    }
  },
  data() {
    return {
      leftColumnHeights: {},
      rightColumnHeights: {},
      scrollTop: 0,
      isScrollLocked: false,
      // ‰∏∫ÊØè‰∏™ÂàÜÁ±ª‰øùÂ≠òÊªöÂä®‰ΩçÁΩÆ
      categoryScrollPositions: {}
    }
  },
  watch: {
    // ÁõëÂê¨Êï∞ÊçÆÂèòÂåñÔºåÊõ¥Êñ∞Âç°ÁâáÈ´òÂ∫¶ÁºìÂ≠ò
    mapData: {
      handler(newData, oldData) {
        this.$nextTick(() => {
          // Âè™‰∏∫Êñ∞Âç°ÁâáÁîüÊàêÈ´òÂ∫¶
          this.generateHeightsForNewItems(newData, oldData)
        })
      },
      deep: true
    },
    // ÁõëÂê¨ÂàÜÁ±ªÂèòÂåñÔºåÊÅ¢Â§çËØ•ÂàÜÁ±ªÁöÑÊªöÂä®‰ΩçÁΩÆ
    activeCategory(newCategory, oldCategory) {
      // ‰øùÂ≠ò‰∏ä‰∏Ä‰∏™ÂàÜÁ±ªÁöÑÊªöÂä®‰ΩçÁΩÆ
      if (oldCategory) {
        this.categoryScrollPositions[oldCategory] = this.getCurrentScrollPosition()
      }
      
      // Âª∂ËøüÊâßË°åÔºåÁ°Æ‰øùDOMÂ∑≤Êõ¥Êñ∞
      this.$nextTick(() => {
        // ÊÅ¢Â§çÂΩìÂâçÂàÜÁ±ªÁöÑÊªöÂä®‰ΩçÁΩÆÔºåÂ¶ÇÊûúÊ≤°ÊúâÂàôËÆæ‰∏∫0
        this.scrollTop = this.categoryScrollPositions[newCategory] || 0
      })
    }
  },
  methods: {
    // Ëé∑ÂèñÂΩìÂâçÊªöÂä®‰ΩçÁΩÆ
    getCurrentScrollPosition() {
      // Ëé∑Âèñscroll-viewÁöÑÊªöÂä®‰ΩçÁΩÆ
      return this.scrollTop
    },
    
    // ÂàÜÁ±ªÂàáÊç¢‰∫ã‰ª∂
    onCategoryChange(categoryId) {
      // ‰øùÂ≠òÂΩìÂâçÂàÜÁ±ªÁöÑÊªöÂä®‰ΩçÁΩÆ
      this.categoryScrollPositions[this.activeCategory] = this.getCurrentScrollPosition()
      
      // Ëß¶ÂèëÂàÜÁ±ªÂàáÊç¢‰∫ã‰ª∂
      this.$emit('category-change', categoryId)
    },
    
    // ÊãñÊãΩ‰∫ã‰ª∂Â§ÑÁêÜ
    onDragStart(e) {
      this.$emit('drag-start', e)
    },
    onDrag(e) {
      this.$emit('drag', e)
    },
    onDragEnd(e) {
      this.$emit('drag-end', e)
    },
    
    // ÂàÜÁ±ªÂàáÊç¢‰∫ã‰ª∂
    onCategoryChange(categoryId) {
      this.$emit('category-change', categoryId)
    },
    
    // ÊêúÁ¥¢ËæìÂÖ•‰∫ã‰ª∂
    onSearchInput(e) {
      this.$emit('search-input', e)
    },
    
    // Âä†ËΩΩÊõ¥Â§ö‰∫ã‰ª∂
    onLoadMore() {
      if (!this.isLoading && !this.isScrollLocked && this.hasMoreData) {
        this.$emit('load-more')
        // ÈîÅÂÆöÊªöÂä®ÔºåÈò≤Ê≠¢ÈáçÂ§çËß¶ÂèëÔºå‰ΩÜÁº©Áü≠ÈîÅÂÆöÊó∂Èó¥
        this.lockScroll()
      }
    },
    
    // ‰∏¥Êó∂ÈîÅÂÆöÊªöÂä®ÔºåÈò≤Ê≠¢ÈáçÂ§çÂä†ËΩΩ
    lockScroll() {
      this.isScrollLocked = true
      setTimeout(() => {
        this.isScrollLocked = false
      }, 500) // Â∞ÜÈîÅÂÆöÊó∂Èó¥‰ªé1000ÊØ´ÁßíÂáèÂ∞ëÂà∞500ÊØ´ÁßíÔºåÊèêÈ´òÂìçÂ∫îÈÄüÂ∫¶
    },
    
    // Âç°ÁâáÁÇπÂáª‰∫ã‰ª∂
    onCardTap(index) {
      this.$emit('card-tap', index)
    },
    
    // ‰∏∫Êñ∞Âç°ÁâáÁîüÊàêÈ´òÂ∫¶
    generateHeightsForNewItems(newData, oldData) {
      // Á°Æ‰øùÊï∞ÊçÆÂ≠òÂú®
      if (!this.mapData) return;
      
      const oldLength = oldData ? oldData.length : 0;
      const newLength = newData ? newData.length : 0;
      
      // Âè™‰∏∫Êñ∞Â¢ûÁöÑÂç°ÁâáÁîüÊàêÈ´òÂ∫¶
      if (newLength > oldLength) {
        // ‰∏∫Â∑¶ÂàóÊñ∞Âç°ÁâáÁîüÊàêÈ´òÂ∫¶
        if (this.leftColumnData && this.leftColumnData.length > 0) {
          this.leftColumnData.forEach((item, index) => {
            const dataIndex = index * 2;
            if (dataIndex >= oldLength && !this.leftColumnHeights[item._id]) {
              this.leftColumnHeights[item._id] = this.getRandomHeight();
            }
          });
        }
        
        // ‰∏∫Âè≥ÂàóÊñ∞Âç°ÁâáÁîüÊàêÈ´òÂ∫¶
        if (this.rightColumnData && this.rightColumnData.length > 0) {
          this.rightColumnData.forEach((item, index) => {
            const dataIndex = index * 2 + 1;
            if (dataIndex >= oldLength && !this.rightColumnHeights[item._id]) {
              this.rightColumnHeights[item._id] = this.getRandomHeight();
            }
          });
        }
      } else if (oldLength === 0 || newLength === 0) {
        // Â¶ÇÊûúÊòØÂÖ®Êñ∞Êï∞ÊçÆÊàñÊ∏ÖÁ©∫Êï∞ÊçÆÔºåÈáçÁΩÆÈ´òÂ∫¶ÁºìÂ≠ò
        this.leftColumnHeights = {};
        this.rightColumnHeights = {};
        
        // ‰∏∫ÊâÄÊúâÂç°ÁâáÁîüÊàêÈ´òÂ∫¶
        if (this.leftColumnData && this.leftColumnData.length > 0) {
          this.leftColumnData.forEach((item) => {
            this.leftColumnHeights[item._id] = this.getRandomHeight();
          });
        }
        
        if (this.rightColumnData && this.rightColumnData.length > 0) {
          this.rightColumnData.forEach((item) => {
            this.rightColumnHeights[item._id] = this.getRandomHeight();
          });
        }
      }
    },
    
    // Ëé∑ÂèñÊåáÂÆöÂàóÂíåÁ¥¢ÂºïÁöÑÂç°ÁâáÈ´òÂ∫¶
    getColumnItemHeight(column, index) {
      const item = column === 'left' ? this.leftColumnData[index] : this.rightColumnData[index]
      if (!item) return 200
      
      const itemId = item._id
      if (column === 'left') {
        if (!this.leftColumnHeights[itemId]) {
          this.leftColumnHeights[itemId] = this.getRandomHeight()
        }
        return this.leftColumnHeights[itemId]
      } else {
        if (!this.rightColumnHeights[itemId]) {
          this.rightColumnHeights[itemId] = this.getRandomHeight()
        }
        return this.rightColumnHeights[itemId]
      }
    },
    
    // ÁîüÊàêÈöèÊú∫È´òÂ∫¶
    getRandomHeight() {
      // ÁîüÊàê180-280‰πãÈó¥ÁöÑÈöèÊú∫È´òÂ∫¶
      return Math.floor(Math.random() * (280 - 180 + 1)) + 180
    }
  },
  computed: {
    // Â∞ÜÊï∞ÊçÆÂàÜ‰∏∫Â∑¶Âè≥‰∏§Âàó
    leftColumnData() {
      return this.mapData ? this.mapData.filter((_, index) => index % 2 === 0) : [];
    },
    rightColumnData() {
      return this.mapData ? this.mapData.filter((_, index) => index % 2 === 1) : [];
    }
  },
}
</script>

